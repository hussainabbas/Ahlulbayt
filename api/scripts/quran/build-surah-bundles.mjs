#!/usr/bin/env node
/**
 * Transform Tanzil source JSON into mobile-compatible SurahBundle files.
 */
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { join } from 'node:path';

import { SURAH_COUNT } from './canonical-ayah-counts.mjs';
import { loadSurahMetadata } from './load-surah-metadata.mjs';
import {
  BUNDLES_DIR,
  MANIFEST_PATH,
  MOBILE_GENERATED_DIR,
  SOURCE_DIR,
  TANZIL_ATTRIBUTION,
} from './paths.mjs';

const BUNDLE_VERSION = 1;

function surahFileName(surah) {
  return `surah-${String(surah).padStart(3, '0')}.json`;
}

async function readSourceSurah(surah) {
  const path = join(SOURCE_DIR, surahFileName(surah));
  const raw = await readFile(path, 'utf8');
  return JSON.parse(raw);
}

function toSurahBundle(meta, source) {
  return {
    surah: meta.number,
    meta: {
      number: meta.number,
      nameArabic: meta.nameArabic,
      nameEnglish: meta.nameEnglish,
      nameUrdu: meta.nameUrdu,
      nameTransliteration: meta.nameTransliteration,
      revelation: meta.revelation,
      ayahCount: meta.ayahCount,
      juzStart: meta.juzStart,
    },
    bundleVersion: BUNDLE_VERSION,
    attribution: {
      arabic: TANZIL_ATTRIBUTION,
      translations: {},
    },
    ayahs: source.ayahs.map((row) => ({
      surah: meta.number,
      ayah: row.ayah,
      juz: meta.juzStart,
      page: 0,
      arabic: row.text,
      words: [],
      translations: {},
      translationSources: {},
    })),
  };
}

async function writeRegistryTs(surahNumbers) {
  const imports = surahNumbers
    .map((n) => `import surah${String(n).padStart(3, '0')} from './surah-${String(n).padStart(3, '0')}.json';`)
    .join('\n');

  const entries = surahNumbers.map((n) => `  ${n}: surah${String(n).padStart(3, '0')},`).join('\n');

  const content = `/** AUTO-GENERATED by api/scripts/quran/build-surah-bundles.mjs — do not edit */
import type { SurahBundle } from '../../types';
${imports}

export const GENERATED_CORPUS_READY = true;

export const GENERATED_SURAHS: Record<number, SurahBundle> = {
${entries}
};

export const GENERATED_SURAH_COUNT = ${surahNumbers.length};
`;

  await writeFile(join(MOBILE_GENERATED_DIR, 'registry.ts'), content, 'utf8');
}

async function main() {
  const metadata = loadSurahMetadata();
  await mkdir(BUNDLES_DIR, { recursive: true });
  await mkdir(MOBILE_GENERATED_DIR, { recursive: true });

  let manifest;
  try {
    manifest = JSON.parse(await readFile(MANIFEST_PATH, 'utf8'));
  } catch {
    throw new Error(`Missing manifest at ${MANIFEST_PATH}. Run npm run quran:fetch first.`);
  }

  const built = [];
  for (let surah = 1; surah <= SURAH_COUNT; surah += 1) {
    const meta = metadata.find((row) => row.number === surah);
    if (!meta) throw new Error(`Missing metadata for surah ${surah}`);

    const source = await readSourceSurah(surah);
    const bundle = toSurahBundle(meta, source);
    const json = `${JSON.stringify(bundle)}\n`;

    const apiPath = join(BUNDLES_DIR, surahFileName(surah));
    const mobilePath = join(MOBILE_GENERATED_DIR, surahFileName(surah));
    await writeFile(apiPath, json, 'utf8');
    await writeFile(mobilePath, json, 'utf8');
    built.push(surah);
  }

  await writeRegistryTs(built);

  const buildManifest = {
    ...manifest,
    bundleVersion: BUNDLE_VERSION,
    builtAt: new Date().toISOString(),
    bundlePaths: {
      api: 'api/data/quran/bundles/',
      mobile: 'mobile/src/features/quran/data/bundled/generated/',
    },
    surahFiles: built.map((n) => surahFileName(n)),
  };

  await writeFile(
    join(BUNDLES_DIR, 'build-manifest.json'),
    `${JSON.stringify(buildManifest, null, 2)}\n`,
    'utf8',
  );

  console.log(`Built ${built.length} surah bundles.`);
  console.log(`API bundles: ${BUNDLES_DIR}`);
  console.log(`Mobile bundles: ${MOBILE_GENERATED_DIR}`);
  console.log('Run npm run quran:validate to verify.');
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
